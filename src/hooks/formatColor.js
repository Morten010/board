export default function formatColor(color) {
    switch(color){
        case "ggr010123":
        return "#3D96CE"
        case "h3gr040123":
        return "#CE433D"
        case "gmg010123":
        return "#2F812B"
        case "h3mg040123":
        return "#CE8B3D"
        case "gwe010123":
        return "#3D96CE"
        case "h1we080122":
        return "#CE433D"
        case "h0mg010123f":
        return "#2F812B"
        case "htxb21":
        return "#CE8B3D"
        default:
        return "#46CE3D"
    }
}
